export const getFrontIndex = (blockIndex) => {
  return blockIndex + 5;
};

export const getFrontRightIndex = (blockIndex) => {
  return blockIndex + 6;
};

export const getRightIndex = (blockIndex) => {
  return blockIndex + 1;
};

export const getBackRightIndex = (blockIndex) => {
  return blockIndex - 4;
};

export const getBackIndex = (blockIndex) => {
  return blockIndex - 5;
};

export const getBackLeftIndex = (blockIndex) => {
  return blockIndex - 6;
};

export const getLeftIndex = (blockIndex) => {
  return blockIndex - 1;
};

export const getFrontLeftIndex = (blockIndex) => {
  return blockIndex + 4;
};
